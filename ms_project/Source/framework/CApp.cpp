#include "CApp.h"
#include "Resource/CResourceHolder.h"
#include "DebugUtil.h"

//=============================================================================
// コンストラクタ
//=============================================================================
CApp::CApp(ISystem* pSystem)
    : m_pSystem(pSystem)
    , m_bIsRunning(true)
{
    _CriticalAssert(!IS_NULL(pSystem));
    CResourceHolder::SetResource(pSystem->GetResource());
}

//=============================================================================
// アプリケーションを終了させる
//=============================================================================
void CApp::Finish()
{
    m_bIsRunning = false;
}

//=============================================================================
// システムの取得
//=============================================================================
ISystem* CApp::GetSystem()
{
    return m_pSystem;
}

