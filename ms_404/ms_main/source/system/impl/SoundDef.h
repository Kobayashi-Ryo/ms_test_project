//*****************************************************************************
// XAudio2ボイスオブジェクト開放処理
//*****************************************************************************
template<class Voice>
void ReleaseVoice(Voice*& pVoice)
{
	if(pVoice)
	{
		pVoice->DestroyVoice();
		pVoice = nullptr;
	}
}

