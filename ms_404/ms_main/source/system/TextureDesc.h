#pragma once
#ifndef __TEXTURE_DESC_H__
#define __TEXTURE_DESC_H__

//*****************************************************************************
// インクルードファイル
//*****************************************************************************
#include <common/common.h>

//*****************************************************************************
// テクスチャーフォーマット
//*****************************************************************************
enum TexFormat
{
    TEXFMT_X8R8G8B8 = 0,
    TEXFMT_A8R8G8B8 = 1
};

//*****************************************************************************
// テクスチャー使用方法
//*****************************************************************************
enum TexUsage
{
    TEXUSAGE_RENDERTARGET = 0,
    TEXUSAGE_DEPTHSTENCIL = 1,
    TEXUSAGE_DYNAMIC      = 2
};

//*****************************************************************************
// テクスチャーメモリー配置方法
//*****************************************************************************
enum TexPool
{
    TEXPOOL_DEFAULT   = 0,
    TEXPOOL_MANAGED   = 1,
    TEXPOOL_SYSTEMMEM = 2
};

//*****************************************************************************
// テクスチャ情報
//*****************************************************************************
struct TextureDesc
{
    TexFormat   Format;
    TexUsage    Usage;
    TexPool     Pool;

    u32         Width;
    u32         Height;
};

#endif  // __TEXTURE_DESC_H__

