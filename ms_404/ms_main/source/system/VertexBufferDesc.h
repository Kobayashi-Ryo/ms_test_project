#pragma once
#ifndef __VERTEXBUFFER_DESC_H__
#define __VERTEXBUFFER_DESC_H__

//*****************************************************************************
// インクルードファイル
//*****************************************************************************
#include <common/common.h>

//*****************************************************************************
// 頂点使用方法
//*****************************************************************************
enum VtxUsage
{
    VTXUSAGE_DYNAMIC = 0,
    VTXUSAGE_SOFTWAREPROCESSING = 1,
    VTXUSAGE_WRITEONLY      = 2
};

//*****************************************************************************
// 頂点メモリー配置方法
//*****************************************************************************
enum VtxPool
{
    VTXPOOL_DEFAULT   = 0,
    VTXPOOL_MANAGED   = 1,
    VTXPOOL_SYSTEMMEM = 2
};

//*****************************************************************************
// 頂点情報
//*****************************************************************************
struct VertexBufferDesc
{
    u32         Size;
    VtxUsage    Usage;
    VtxPool     Pool;
};

#endif  // __VERTEXBUFFER_DESC_H__

