#pragma once
#ifndef __INDEXBUFFER_DESC_H__
#define __INDEXBUFFER_DESC_H__

//*****************************************************************************
// インクルードファイル
//*****************************************************************************
#include <common/common.h>

//*****************************************************************************
// インデックスバッファフォーマット
//*****************************************************************************
enum IdxFormat
{
    IDXFMT_INDEX16 = 0,
    IDXFMT_INDEX32 = 1
};

//*****************************************************************************
// インデックスバッファ使用方法
//*****************************************************************************
enum IdxUsage
{
    IDXUSAGE_DYNAMIC = 0,
    IDXUSAGE_WRITEONLY = 1
};

//*****************************************************************************
// インデックスバッファメモリー配置方法
//*****************************************************************************
enum IdxPool
{
    IDXPOOL_DEFAULT   = 0,
    IDXPOOL_MANAGED   = 1,
    IDXPOOL_SYSTEMMEM = 2
};

//*****************************************************************************
// インデックスバッファ情報
//*****************************************************************************
struct IndexBufferDesc
{
    u32         Size;
    IdxFormat   Format;
    IdxUsage    Usage;
    IdxPool     Pool;
};

#endif  // __INDEXBUFFER_DESC_H__

